int	ft_is_prime(int nb)
{
	int	index;

	index = 3;
	if (nb <= 1)
		return (0);
	if (nb == 2)
		return (1);
	if (nb % 2 == 0 || nb % 3 == 0 || nb % 5 == 0)
		return (0);
	while (nb / index >= index)
	{
		if (nb % index == 0)
			return (0);
		index += 2;
	}
	return (1);
}
